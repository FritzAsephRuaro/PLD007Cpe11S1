#include <iostream>
using namespace std;

int main(){
	
	cout<<"	Algorithm to get the sum, difference, product, quotient, and remainder of two integer"<<endl;
	
	int x = 4, y = 2;
	cout<<"	x + y = "<< x + y << endl;
	cout<<"	x - y = "<< x - y <<endl;
	cout<<"	x * y = "<< x * y <<endl;
	cout<<"	x / y = "<< x / y <<endl;
	cout<<"	x % y = "<< x % y <<endl;
	
	return 0;
}
